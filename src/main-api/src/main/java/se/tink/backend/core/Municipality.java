package se.tink.backend.core;

public class Municipality {
    private String code;
    private String name;
    
    public Municipality() {
        
    }

    public String getCode() {
        return code;
    }

    public String getName() {
        return name;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public void setName(String name) {
        this.name = name;
    }
}
